using System;
using System.Collections.Generic;
using System.Text;
using System.IO;

namespace SyncFoldersApi
{
    //*******************************************************************************************************
    /// <summary>
    /// Objects that implement this interface provide information about files
    /// </summary>
    //*******************************************************************************************************
    public interface IFileInfo
    {
        //===================================================================================================
        /// <summary>
        /// Last write time
        /// </summary>
        //===================================================================================================
        DateTime LastWriteTimeUtc { get; set; }

        //===================================================================================================
        /// <summary>
        /// Does the file exist?
        /// </summary>
        //===================================================================================================
        bool Exists { get; }

        //===================================================================================================
        /// <summary>
        /// Length of the file
        /// </summary>
        //===================================================================================================
        long Length {get; }

        //===================================================================================================
        /// <summary>
        /// Full path of the file
        /// </summary>
        //===================================================================================================
        string FullName { get; }

        //===================================================================================================
        /// <summary>
        /// Deletes the file
        /// </summary>
        //===================================================================================================
        void Delete();


        //===================================================================================================
        /// <summary>
        /// Moves the file to a new location
        /// </summary>
        /// <param name="strNewPath">New path for the file</param>
        //===================================================================================================
        void MoveTo(string strNewPath);


        //===================================================================================================
        /// <summary>
        /// Copies the file
        /// </summary>
        /// <param name="strDestPath">Destination path for the file</param>
        /// <param name="bOverwrite">Specifies, if the file shall be overwritten</param>
        //===================================================================================================
        IFileInfo CopyTo(
            string strDestPath,
            bool bOverwrite
            );

        //===================================================================================================
        /// <summary>
        /// Copies the file
        /// </summary>
        /// <param name="strDestPath">Destination path for the file</param>
        //===================================================================================================
        IFileInfo CopyTo(
            string strDestPath
            );

        //===================================================================================================
        /// <summary>
        /// Name of the file
        /// </summary>
        //===================================================================================================
        string Name
        {
            get;
        }

        //===================================================================================================
        /// <summary>
        /// Extension of the file name
        /// </summary>
        //===================================================================================================
        string Extension
        {
            get;
        }

        //===================================================================================================
        /// <summary>
        /// Directory of the file
        /// </summary>
        //===================================================================================================        
        string DirectoryName
        {
            get;
        }


        //===================================================================================================
        /// <summary>
        /// Directory of the file
        /// </summary>
        //===================================================================================================        
        IDirectoryInfo Directory
        {
            get;
        }

        //===================================================================================================
        /// <summary>
        /// Gets or sets file attributes
        /// </summary>
        //===================================================================================================
        FileAttributes Attributes
        {
            get;
            set;
        }
    }

}
